# Tests for WinnerTracker

import unittest
from src.core import Core

class TestCore(unittest.TestCase):
    def setUp(self):
        self.core = Core()
    
    def test_initialization(self):
        self.assertTrue(self.core.initialized)
    
    def test_status(self):
        status = self.core.get_status()
